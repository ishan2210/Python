"""
Slicing You can return a range of characters by using the slice syntax.
Specify the start index and the end index, separated by a colon, to return a part of the string.
"""
b="Hello world"
print(b[2:5])

#slicing from start
print(b[:5])

#slicing from the end
print(b[2:])

#negative indexing
print(b[-5:-2])

