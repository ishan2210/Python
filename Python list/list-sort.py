#List objects have a sort() method that will sort the list alphanumerically, ascending, by default:
thislist=["apple","mango","chickoo","banana","orange"]
thislist.sort()
print(thislist)

#Sort the list numerically:
thislist=[55,77,99,66,22]
thislist.sort()
print(thislist)

#Sort the list descending:
thislist=["apple","chickoo","orange","jamun","banana"]
thislist.sort(reverse=True)
print(thislist)

